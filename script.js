let currentLang = localStorage.getItem('lang') || 'pt';

function setLang(lang) {
    currentLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang;

    // Update all data-i18n elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (i18n[lang] && i18n[lang][key]) {
            el.innerHTML = i18n[lang][key];
        }
    });

    // Update the h1 greeting (not a data-i18n element, just the first text node of h1)
    const h1 = document.querySelector('.hero h1');
    if (h1) {
        const greeting = i18n[lang]['hero.greeting'];
        h1.innerHTML = `${greeting}<br><span class="gradient-text typewriter" id="typewriter">${i18n[lang]['typewriter']}</span>`;
    }

    // Active button highlighting
    ['pt','en','es'].forEach(l => {
        const btn = document.getElementById('btn-' + l);
        if (btn) btn.classList.toggle('active', l === lang);
    });
}

// Init on load
document.addEventListener('DOMContentLoaded', () => { setLang(currentLang); });

