// ===== SMOOTH REVEAL (IntersectionObserver) =====
const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry, i) => {
        if (entry.isIntersecting) {
            const el = entry.target;
            const delay = el.dataset.delay || 0;
            setTimeout(() => el.classList.add('active'), delay);
            observer.unobserve(el);
        }
    });
}, { threshold: 0.1 });

document.querySelectorAll('.reveal').forEach(el => observer.observe(el));

// Staggered reveal for child items
const childObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const parent = entry.target;
            parent.querySelectorAll('.reveal-item').forEach((item, i) => {
                setTimeout(() => item.classList.add('active'), i * 120);
            });
            childObserver.unobserve(parent);
        }
    });
}, { threshold: 0.05 });

document.querySelectorAll('.project-grid, .timeline').forEach(el => childObserver.observe(el));

